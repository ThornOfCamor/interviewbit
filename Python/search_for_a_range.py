class Solution:
    # @param A : list of integers
    # @param B : integer
    # @return an integer
    
    def low_bin_search(self, A, B, low, high):
        if B <= A[low]:
            return low
        if B > A[high]:
            return high + 1
        if low==high:
            if A[low] == B:
                return low
            else:
                return -1
        mid = int((low+high)/2)
        if B <= A[mid]:
            return self.low_bin_search(A, B, low, mid-1)
        return self.low_bin_search(A, B, mid+1, high)

    def high_bin_search(self, A, B, low, high):
        if B == A[high]:
            return high
        if B < A[low]:
            return low - 1
        if B > A[high]:
            return high + 1
        if low==high:
            if A[low] == B:
                return low
            else:
                return -1
        mid = int((low+high)/2)
        if B >= A[mid]:
            return self.high_bin_search(A, B, mid+1, high)
        return self.high_bin_search(A, B, low, mid-1)
        
    
    def searchRange(self, A, B):
        if B < A[0]:
            return [-1, -1]
        elif B > A[len(A)-1]:
            return [-1, -1]
        if A[0] == B:
            low = 0
        else :
            low = self.low_bin_search(A, B, 0, len(A)-1)
        if A[len(A)-1] == B:
            high = len(A) - 1
        else :
            high = self.high_bin_search(A, B, 0, len(A)-1)
        if low <= high and A[low] == A[high]: 
            return [low, high]
        else:
            return [-1, -1]

